BATCH_SIZE = 50
def bulk_import():
    total = len(notes)
    print(f"ðŸš€ Starting bulk import of {total} notes in batches of {BATCH_SIZE}...")

    for i in range(0, total, BATCH_SIZE):
        batch = notes[i : i + BATCH_SIZE]

        ids = []
        documents = []
        embeddings = []
        metadatas = []

        for title, content in batch:
            vector = embed_text(content)
            note_id = str(uuid4())
            now = datetime.now(timezone.utc).isoformat()

            ids.append(note_id)
            documents.append(content)
            embeddings.append(vector)
            metadatas.append({
                "title": title,
                "created_at": now,
                "updated_at": now
            })

        # âœ… Insert batch
        notes_collection.add(
            ids=ids,
            documents=documents,
            embeddings=embeddings,
            metadatas=metadatas
        )

        print(f"âœ… Imported {len(batch)} notes (batch {i//BATCH_SIZE + 1})")

    print("ðŸŽ‰ Bulk import completed successfully!")


